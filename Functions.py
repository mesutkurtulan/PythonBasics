# Functions declaration

def Greeting():
    print("Hello World")

# Function call
Greeting()                  # Hello World

def GreetingToUser(name):
    print(f"Hello {name}")

GreetingToUser("John")      # Hello John

def SumIntegers(num1, num2):
    return num1 + num2

print(SumIntegers(1, 2))        # 3